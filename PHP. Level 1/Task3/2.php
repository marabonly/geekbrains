<?php

/*
2. С помощью цикла do…while написать функцию для вывода чисел от 0 до 10, чтобы результат выглядел так:
0 – это ноль.
1 – нечетное число.
2 – четное число.
3 – нечетное число.
…
10 – четное число.
*/

$number = -1;

do {
    $number++;
    if ($number === 0) echo $number . ' - это ноль.';
    else if ($number % 2 === 0) echo '<br>' . $number . ' - это чётное число.';
    else echo '<br>' . $number . ' - это нечётное число.';
} while ($number < 10);
